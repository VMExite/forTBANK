CREATE TABLE chat (
    chat_id BIGINT PRIMARY KEY
);

CREATE TABLE link
(
    link_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY ,
    last_update TIMESTAMP(6) WITH TIME ZONE NOT NULL ,
    url VARCHAR(2048) NOT NULL UNIQUE
);

CREATE TABLE tag
(
    tag_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(255) NOT NULL UNIQUE
);

CREATE TABLE chat_link
(
    link_id BIGINT NOT NULL REFERENCES link ON DELETE CASCADE,
    chat_id BIGINT NOT NULL REFERENCES chat ON DELETE CASCADE,
    PRIMARY KEY (link_id, chat_id)
);

CREATE TABLE link_tag
(
    link_id BIGINT NOT NULL REFERENCES link ON DELETE CASCADE,
    tag_id BIGINT NOT NULL REFERENCES tag ON DELETE CASCADE,
    PRIMARY KEY (link_id, tag_id)
);

