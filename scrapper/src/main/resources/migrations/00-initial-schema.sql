CREATE TABLE chat (
    chat_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY
);

CREATE TABLE link
(
    link_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY ,
    last_update TIMESTAMP(6) WITH TIME ZONE NOT NULL ,
    url VARCHAR(255) NOT NULL
);

CREATE TABLE tag
(
    tag_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(255) NOT NULL
);

CREATE TABLE chat_link
(
    link_id BIGINT NOT NULL REFERENCES link,
    chat_id BIGINT NOT NULL REFERENCES chat
);

CREATE TABLE link_tag
(
    link_id BIGINT NOT NULL REFERENCES link,
    tag_id BIGINT NOT NULL REFERENCES tag
);

